<p>The idea to host the con was not some lightning bolt that struck us, it just came out of a very casual conversation about conventions in general. We thought it would be a good idea to have a local convention so we organised one and people came.</p>
<p>For the most part it’s still largely like that. Although experience has taught us more efficient ways to do things it’s still very much a grassroots event. We don’t have a lot of sponsorship nor do we do a lot of advertising. Despite this, we’re still here five years later and that’s largely because of word-of-mouth recommendations and keeping our costs as low as possible.</p>
<p>A big part of the ethos of IntrigueCon is that that everyone is in this together. If roleplayers want Edmonton to have an annual convention then we need everyone, GMs and players, to pitch in. One can’t function without the other so we don’t differentiate.</p>
<p>This ethos is also a crucial component of keeping the convention afloat financially. While some other conventions offer discounts for GMs, after losing a lot of money in the first year, there was no way we could continue to do this. The organising of IntrigueCon was never intended to be a commercial venture, but equally we couldn’t continue to put in the hundreds of hours it takes to make IntrigueCon work for the purposes of getting a bill at the end. At the very least the registrations had to cover the costs.</p>
<p>That said, our goal is always to offer a little something extra to GMs in recognition of the work that they put in to create a game, but we trust that GMs are doing putting in that work because they enjoy it. If you’re not into that particular variety of “lonely fun”, then come to IntrigueCon as a player. We’d love to have you.</p>
<p>Considering all this, our plan to keep IntrigueCon alive is simple, and it also happens to be what we’re all about:</p>
<ul>
    <li>Organising a consistently fun, well-organized and inclusive event</li>
    <li>Offering as wide a variety of games for people to try as possible</li>
    <li>Creating a roleplaying event that encourages roleplayers get to know other roleplayers in the city and beyond.</li>
    <li>Creating an environment that fosters positive experiences for first-time roleplayers so they become regular roleplayers.</li>
</ul>

<p>And that’s all you need to know About Us and the Convention. We look forward to seeing you there so that you can check it out in person.</p>
